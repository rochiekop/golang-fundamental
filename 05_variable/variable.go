package main

import "fmt"

func main() {
	var name string

	name = "Rochi Eko"
	fmt.Println(name)

	name = "Eko Pambudi"
	fmt.Println(name)

	
	var name_ = "Rochi"
	fmt.Println(name_)

	name_ = "Eko"
	fmt.Println(name_)

	name__ := "Rochi Eko Pambudi"
	fmt.Println(name__)

	name__ = "12"
	fmt.Println(name__)

	var (
		firstName = "Rochi"
		lastName = "Eko Pambudi"
	)
	fmt.Println(firstName, lastName)
	
}
